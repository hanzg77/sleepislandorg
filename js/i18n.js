// Detect user language
function detectLang() {
  // Check URL hash first (e.g., #lang=ja)
  const hash = window.location.hash;
  const hashMatch = hash.match(/lang=(\w+)/);
  if (hashMatch && I18N[hashMatch[1]]) return hashMatch[1];

  // Check localStorage
  const saved = localStorage.getItem('si_lang');
  if (saved && I18N[saved]) return saved;

  // Check URL path
  const path = window.location.pathname;
  if (path.startsWith('/en/') || path === '/en') return 'en';
  if (path.startsWith('/ja/') || path === '/ja') return 'ja';
  if (path.startsWith('/es/') || path === '/es') return 'es';
  if (path.startsWith('/pt/') || path === '/pt') return 'pt';

  // Check browser language
  const navLang = (navigator.language || navigator.userLanguage || '').toLowerCase();
  if (navLang.startsWith('zh')) return 'zh';
  if (navLang.startsWith('ja')) return 'ja';
  if (navLang.startsWith('es')) return 'es';
  if (navLang.startsWith('pt')) return 'pt';
  if (navLang.startsWith('en')) return 'en';

  // Default
  return 'en';
}

let currentLang = detectLang();

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || (I18N.en && I18N.en[key]) || key;
}

function getSoundName(id) {
  return (SOUND_NAMES[currentLang] && SOUND_NAMES[currentLang][id]) || SOUND_NAMES.en[id] || id;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem('si_lang', lang);
  applyTranslations();
  renderSounds();
}

function applyTranslations() {
  document.documentElement.lang = t('htmlLang');
  document.title = t('pageTitle');

  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });

  // Update all elements with data-i18n-href
  document.querySelectorAll('[data-i18n-href]').forEach(el => {
    const key = el.getAttribute('data-i18n-href');
    el.href = t(key);
  });

  // Update language switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}
