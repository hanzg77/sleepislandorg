export function getResourceTitle(slug: string, lang: Lang, resources: Record<string, any>): string {
  const res = resources[slug];
  if (!res) return slug;
  if (lang === 'zh-Hans') return res.title;
  if (lang === 'zh-Hant') return RESOURCE_TITLES['zh-Hant']?.[slug] || res.title;
  if (lang === 'en') return res.titleEn || res.title;
  return RESOURCE_TITLES[lang]?.[slug] || res.titleEn || res.title;
}

export function getScenes(scenes: string[], lang: Lang): string {
  if (lang === 'zh-Hans' || lang === 'zh-Hant') return scenes.join('、');
  const map = SCENE_MAP[lang] || SCENE_MAP.en;
  return scenes.map(s => map[s] || s).join(lang === 'en' ? ', ' : '、');
}
